use cosmwasm_std::StdError;
use thiserror::Error;

#[derive(Error, Debug, PartialEq)]
pub enum ContractError {
    #[error("{0}")]
    Std(#[from] StdError),

    #[error("Unauthorized")]
    Unauthorized {},

    #[error("Cannot set zero amount")]
    InvalidZeroAmount {},

    #[error("Insufficient funds")]
    InsufficientFunds {},
}